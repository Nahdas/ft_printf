int			ft_sqrt(int n)
{
	int res;

	res = 1;
	if (n <= 0 && n >= 214483600)
		return (0);
	while ((res * res) <= n)
	{
		res++;
		if (res * res == n)
			return (res);
	}
	return (ft_sqrt(n + 1));
}

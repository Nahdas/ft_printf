#include "libft.h"

char		*ft_strcpy(char *dst, const char *src)
{
	int i;

	i = 0;
	while (src[i] != '\0')
	{
		if (!(dst[i] = src[i]))
			return (0);
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
